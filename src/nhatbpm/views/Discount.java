    private void loadData() {
        DiscountDAO dao = new DiscountDAO();
        ArrayList<DiscountDTO> list;
        String header[] = {"Dis Code", "Percent", "Date"};
        DefaultTableModel tblModel = new DefaultTableModel(header, 0);
        try {
            list = dao.getDiscountList();
            for (DiscountDTO dto : list) {
                tblModel.addRow(dto.toVector());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        tblDiscount.setModel(tblModel);
    }

