    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String code = txtCode.getText().trim().toUpperCase();
        float percent = Float.valueOf(txtPercent.getText());
        String discription = txtDiscription.getText();
        String date = txtDate.getText();
        DiscountDAO dao = new DiscountDAO();
        try {
            if (dao.contains(code)) {
                JOptionPane.showMessageDialog(this, "Code is exist!!!");
                return;
            }
            dao.create(code, percent, discription, date);
            JOptionPane.showMessageDialog(this, "Create successfull!!!");
        } catch (Exception e) {
            e.printStackTrace();
        }
        loadData();
        txtCode.setEditable(false);
        btnCreate.setEnabled(false);
        btnRemove.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String code = txtCode.getText().trim().toUpperCase();
        float percent = Float.valueOf(txtPercent.getText());
        String discription = txtDiscription.getText();
        String date = txtDate.getText();
        DiscountDAO dao = new DiscountDAO();
        try {
            dao.update(code, percent, discription, date);
            JOptionPane.showMessageDialog(this, "Update successfull!!!");
        } catch (Exception e) {
            e.printStackTrace();
        }
        loadData();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        String code = txtCode.getText().trim().toUpperCase();
        DiscountDAO dao = new DiscountDAO();
        try {
            dao.delete(code);
        } catch (Exception e) {
            e.printStackTrace();
        }
        loadData();
    }//GEN-LAST:event_btnRemoveActionPerformed

