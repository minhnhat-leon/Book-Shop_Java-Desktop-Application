    private void tblDiscountMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDiscountMouseClicked
        int selectedIndex = tblDiscount.getSelectedRow();
        String code = tblDiscount.getValueAt(selectedIndex, 0).toString();
        DiscountDAO dao = new DiscountDAO();
        try {
            DiscountDTO dto = dao.getDiscount(code);
            txtCode.setText(code);
            txtPercent.setText(dto.getPrecent() + "");
            txtDiscription.setText(dto.getDescription());
            txtDate.setText(dto.getDateStar());
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        txtCode.setEditable(false);
        btnCreate.setEnabled(false);
        btnRemove.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tblDiscountMouseClicked

