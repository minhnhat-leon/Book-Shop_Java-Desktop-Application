    private void loadData() {
        BookDetailsDAO bookDao = new BookDetailsDAO();
        BookDTO bookDto;
        try {
            bookDto = bookDao.getBook(cartDto.getBookID());
            lbTitle.setText(bookDto.getTitle());
            lbAuthor.setText(bookDto.getAuthor());
            lbCategory.setText(bookDto.getCategoryID());
            lbPrice.setText(bookDto.getPrice() + " VNĐ");
            lbDate.setText(cartDto.getOderDate());
            
            if (frameCall.equals("History")) {
                btnRemove.setVisible(false);
                lbAmount.setText(cartDto.getQuantity()+"");
                jAmount.setVisible(false);
            } else {
                lbAmount.setText("");
                if (bookDto.getQuantity() < cartDto.getQuantity()) {
                    jAmount.setValue(bookDto.getQuantity());
                } else {
                    jAmount.setValue(cartDto.getQuantity());
                }
            }
            lbTotal.setText(bookDto.getPrice()*cartDto.getQuantity() + " VNĐ");
        } catch (Exception e) {
        }
    }
            
