    private void jAmountStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jAmountStateChanged
        CartDAO cartDao = new CartDAO();
        BookDetailsDAO bookDao = new BookDetailsDAO();
        BookDTO bookDto;
        try {
            bookDto = bookDao.getBook(cartDto.getBookID());
            int newAmount = Integer.parseInt(jAmount.getValue().toString());
            if (bookDto.getQuantity() < newAmount) {
                JOptionPane.showMessageDialog(this, bookDto.getTitle() + "\nOnly " + bookDto.getQuantity() + " products left. \nPlease!!! Select the number of products again");
                jAmount.setValue(bookDto.getQuantity());
            } else {
                cartDao.updateItem(cartDto.getBookID(), newAmount);
                lbTotal.setText(bookDto.getPrice()*newAmount + " VNĐ");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jAmountStateChanged


